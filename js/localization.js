        currentLanguage = 'bn';
        document.getElementById('languageToggle').innerHTML = `en<i class="fa fa-toggle-on fa-2x" style="margin-right: 7px;margin-left: 7px;"></i>bn`;
        changeLanguage('bn'); // Call your language-changing function
